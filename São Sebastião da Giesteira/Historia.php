        <h2 class="PrimeiroH">História do Centro Social e Paroquial de São Sebastião da Giesteira</h2>
        <p class="pTextMain">São Sebastião da Giesteira, é uma aldeia que fica localizada numa freguesia rural do concelho de Évora, na altura necessitava do nascimento do Centro Social de serviços adequados para dar resposta às necessidades laborais e sociais da população residente e dos meios envolventes.</p>
        <p class="pTextMain">Nasceu assim o Centro Social e Paroquial de São Sebastião da Giesteira que é uma Instituição de Solidariedade Social aprovada a 27 de setembro de 1991 e registada nas Fundações de Solidariedade Social a 22 de novembro de 1991.</p>
        <p class="pTextMain">A primeira atividade deste Centro nasceu de um protocolo com o Instituto Português da Juventude para a ocupação de Atividades de tempos livres em 1995 que funcionava na Sacristia da Igreja, com 20 crianças e adolescentes. Entretanto algumas funcionárias moradoras da aldeia, davam apoio aos utentes da povoação. Esta grupo pertencia ao Centro Social e Paroquial da Nossa Senhora da Boa-fé. Com a doação do terreno anexo á igreja fez com que se iniciasse o projeto arquitetónico que foi inaugurado em 17 de outubro de 1997, constituído por ATL, Centro de Dia, cozinha, salão, lavandaria e salão comunitário, o que permitia a transferência do ATL, para as novas instalações e a vinda dos utentes do Centro de Dia que estavam provisoriamente no Centro Social e Paroquial da Nossa Senhora da Boa-fé.</p>
        <p class="pTextMain">A direção do Centro Social de São Sebastião começou de imediato a idealizar construção do lar. Sem apoios financeiros e mais uma vez com a ajuda de benfeitores deu-se o início à segunda fase da obra, construindo onze quartos, enfermaria, sala de tratamento e um quarto privado com uma pequena capela, para receber uma comunidade religiosa vinda do Brasil, cuja inauguração aconteceu a 8 de Março de 2003, pelo Arcebispo de Évora, D. Maurílio de Gouveia.</p>
        <p class="pTextMain">Desde a abertura do Centro, que se contava com a presença de uma comunidade religiosa das Irmãs de Nossa Senhora da Glória, vindas do Brasil, constituída pelas Irmãs Maria das Graças e Cícera.</p>
        <p class="pTextMain">Em 30 de Setembro de 2007 por decisão da congregação das Irmãs de Nossa Senhora da Glória, por doença de uma das irmãs e por não haver quem as substituísse, as Irmãs partiram, mas a missão continuou.</p>
        <p class="pTextMain">A criação deste equipamento social permitiu a não desertificação da aldeia bem como a criação de cerca de 20 postos de trabalho. Neste meio rural, muitas vezes as dificuldades acentuam-se, a existência destes equipamentos sociais vêm combater o isolamento social, uma vez que a procura da institucionalização é cada vez mais frequente.</p>
        <p class="pTextMain">Assim sendo, a sua área de intervenção principal é a gerontologia.</p>
